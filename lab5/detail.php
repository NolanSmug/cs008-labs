                <h2>Airplanes</h2>
                <p>Although airplanes also release heavy amounts of carbon dioxide into the atmosphere, jet engines also release water vapor, nitrous gases, and particles that
                reduce AQI (air quality). Water vapor itself does not live long in the atmosphere, however contrails that form from planes are the bigger issue. Under low temperatures,
                water vapor released from jet engines freezes creating lined contrails. These clouds stretch across the sky and trap infrared rays warming up the atmosphere 3 times more 
                than carbon dioxide.</p>
                    
